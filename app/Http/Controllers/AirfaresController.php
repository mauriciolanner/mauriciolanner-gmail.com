    public function create(Request $request)
    {
        //status da compra da passagem
        //1 - no corrinho
        //2 - pago
        //3 - cancelado
        //4 - Reembolsado

        $airfare = new Airfare;

        $airfare->travels_id = $request->travels_id;
        $airfare->users_id = auth()->user()->id;
        $airfare->status = 1;
        $airfare->class = $request->clas;

        $airfare->save();

        return 'adicionado';
    }

